-- Benchmark Schema for ID Correlation Testing
-- Extends e-commerce schema with UUID, IDENTITY, and cross-session patterns
-- Run AFTER init-db-a.sql or init-db-b.sql

-- Enable UUID extension
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ═══════════════════════════════════════════════════
-- Table with UUID primary key (tests UUID correlation)
-- ═══════════════════════════════════════════════════
CREATE TABLE tracking_events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    order_id INTEGER REFERENCES orders(id),
    event_type TEXT NOT NULL,
    payload JSONB DEFAULT '{}',
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_tracking_order_id ON tracking_events(order_id);
CREATE INDEX idx_tracking_event_type ON tracking_events(event_type);

-- ═══════════════════════════════════════════════════
-- Table with GENERATED ALWAYS AS IDENTITY (tests identity correlation)
-- ═══════════════════════════════════════════════════
CREATE TABLE audit_log (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    table_name TEXT NOT NULL,
    operation TEXT NOT NULL,       -- INSERT, UPDATE, DELETE
    record_id TEXT NOT NULL,       -- the ID of the affected record (string for UUID support)
    old_values JSONB,
    new_values JSONB,
    performed_by TEXT DEFAULT current_user,
    performed_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_audit_table_op ON audit_log(table_name, operation);
CREATE INDEX idx_audit_record ON audit_log(record_id);

-- ═══════════════════════════════════════════════════
-- Shared cart: cross-session ID dependency pattern
-- Session A creates cart, Session B adds items referencing that cart ID
-- ═══════════════════════════════════════════════════
CREATE TABLE shared_carts (
    id SERIAL PRIMARY KEY,
    owner_customer_id INTEGER NOT NULL REFERENCES customers(id),
    share_token TEXT NOT NULL UNIQUE DEFAULT encode(gen_random_bytes(16), 'hex'),
    status TEXT NOT NULL DEFAULT 'active',
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE shared_cart_items (
    id SERIAL PRIMARY KEY,
    cart_id INTEGER NOT NULL REFERENCES shared_carts(id),
    product_id INTEGER NOT NULL REFERENCES products(id),
    added_by_customer_id INTEGER NOT NULL REFERENCES customers(id),
    qty INTEGER NOT NULL DEFAULT 1,
    added_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_shared_carts_token ON shared_carts(share_token);
CREATE INDEX idx_shared_cart_items_cart ON shared_cart_items(cart_id);

-- ═══════════════════════════════════════════════════
-- Table with composite PK (tests composite key correlation)
-- ═══════════════════════════════════════════════════
CREATE TABLE inventory_movements (
    warehouse_id INTEGER NOT NULL,
    product_id INTEGER NOT NULL REFERENCES products(id),
    movement_seq SERIAL,
    qty_change INTEGER NOT NULL,
    reason TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (warehouse_id, product_id, movement_seq)
);

-- ═══════════════════════════════════════════════════
-- Table with GENERATED BY DEFAULT (allows explicit or auto ID)
-- ═══════════════════════════════════════════════════
CREATE TABLE notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id INTEGER NOT NULL REFERENCES customers(id),
    channel TEXT NOT NULL DEFAULT 'email',
    subject TEXT NOT NULL,
    body TEXT,
    sent_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_notifications_customer ON notifications(customer_id);

